package javaBasic;

public class VariableExam {
		
	public static void main(String[] args) {
		int count; // 정수값을 담을 수 있는 count라는 변수 선언
		count = 10;
		count = 20;
		System.out.println(count); //20
		
		double avg; // 실수값을 담을 수 있는 avg 라는 변수 선언
		avg = 11.1;
		System.out.println(avg); //11.1
		
		String name = "yoonho"; // 문자열을 담을 수 있는 name 라는 변수 선언
		System.out.println(name); // yoonho
	}
}
