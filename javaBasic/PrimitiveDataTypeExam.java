package javaBasic;

public class PrimitiveDataTypeExam {
	
	public static void main(String[] args) {
		boolean isT = true;
		boolean isF = false;
		
		// boolean(논리형)은 크기가 1byte이고 true와 false 중에 한 가지 값을 가질 수 있다.
		
		char c = 'f';
		// 문자형은 char로 2byte크기를 가집니다. 문자는 작은따옴표를 이용하여 한 글자를 표현할 수 있다.
		int x = 59;
		// 정수형 int는 4바이트이다.
		long bing = 434345345L;
		// 정수형 long은 8byte이다. 뒤에 소문자 l이나 대문자 L
		float f = 54.3f;
		// 실수형 float은 4바이트이다. 뒤에 소문자 f나 대문자 F
		double d = 23423423424.22344211;
		// 실수형 double은 8바이트이다.
	}
}
